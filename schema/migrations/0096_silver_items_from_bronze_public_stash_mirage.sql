CREATE MATERIALIZED VIEW IF NOT EXISTS poe_trade.mv_bronze_public_stash_mirage_to_silver_items
TO poe_trade.silver_items AS
WITH aggregated AS (
    SELECT
        checkpoint,
        root_item_id,
        maxIf(ingested_at, row_type = 'item') AS observed_at,
        argMaxIf(stash_id, ingested_at, row_type = 'item') AS stash_id,
        argMaxIf(account_name, ingested_at, row_type = 'item') AS account_name,
        argMaxIf(stash_name, ingested_at, row_type = 'item') AS stash_name,
        argMaxIf(stash_type, ingested_at, row_type = 'item') AS stash_type,
        maxIf(root_item_ordinal, row_type = 'item') AS root_item_ordinal,
        argMaxIf(item_name, ingested_at, row_type = 'item') AS root_item_name,
        argMaxIf(item_type_line, ingested_at, row_type = 'item') AS root_item_type_line,
        argMaxIf(base_type, ingested_at, row_type = 'item') AS root_base_type,
        argMaxIf(rarity, ingested_at, row_type = 'item') AS root_rarity,
        argMaxIf(inventory_id, ingested_at, row_type = 'item') AS root_inventory_id,
        argMaxIf(note, ingested_at, row_type = 'item') AS root_note,
        argMaxIf(icon, ingested_at, row_type = 'item') AS root_icon,
        argMaxIf(descr_text, ingested_at, row_type = 'item') AS descr_text,
        argMaxIf(sec_descr_text, ingested_at, row_type = 'item') AS sec_descr_text,
        argMaxIf(art_filename, ingested_at, row_type = 'item') AS art_filename,
        argMaxIf(built_in_support, ingested_at, row_type = 'item') AS built_in_support,
        argMaxIf(item_colour, ingested_at, row_type = 'item') AS item_colour,
        argMaxIf(stack_size_text, ingested_at, row_type = 'item') AS stack_size_text,
        argMaxIf(ilvl, ingested_at, row_type = 'item') AS root_ilvl,
        argMaxIf(item_level, ingested_at, row_type = 'item') AS item_level,
        argMaxIf(monster_level, ingested_at, row_type = 'item') AS monster_level,
        argMaxIf(stack_size, ingested_at, row_type = 'item') AS root_stack_size,
        argMaxIf(max_stack_size, ingested_at, row_type = 'item') AS max_stack_size,
        argMaxIf(frame_type, ingested_at, row_type = 'item') AS root_frame_type,
        argMaxIf(w, ingested_at, row_type = 'item') AS w,
        argMaxIf(h, ingested_at, row_type = 'item') AS h,
        argMaxIf(x, ingested_at, row_type = 'item') AS x,
        argMaxIf(y, ingested_at, row_type = 'item') AS y,
        argMaxIf(socket_index, ingested_at, row_type = 'item') AS root_socket_index,
        argMaxIf(talisman_tier, ingested_at, row_type = 'item') AS talisman_tier,
        argMaxIf(foil_variation, ingested_at, row_type = 'item') AS foil_variation,
        argMaxIf(verified, ingested_at, row_type = 'item') AS root_verified,
        argMaxIf(identified, ingested_at, row_type = 'item') AS root_identified,
        argMaxIf(corrupted, ingested_at, row_type = 'item') AS root_corrupted,
        argMaxIf(fractured, ingested_at, row_type = 'item') AS root_fractured,
        argMaxIf(synthesised, ingested_at, row_type = 'item') AS synthesised,
        argMaxIf(duplicated, ingested_at, row_type = 'item') AS duplicated,
        argMaxIf(split_flag, ingested_at, row_type = 'item') AS split_flag,
        argMaxIf(support_flag, ingested_at, row_type = 'item') AS support_flag,
        argMaxIf(abyss_jewel, ingested_at, row_type = 'item') AS abyss_jewel,
        argMaxIf(delve, ingested_at, row_type = 'item') AS delve,
        argMaxIf(elder, ingested_at, row_type = 'item') AS elder,
        argMaxIf(shaper, ingested_at, row_type = 'item') AS shaper,
        argMaxIf(searing, ingested_at, row_type = 'item') AS searing,
        argMaxIf(tangled, ingested_at, row_type = 'item') AS tangled,
        argMaxIf(veiled, ingested_at, row_type = 'item') AS veiled,
        argMaxIf(memory_item, ingested_at, row_type = 'item') AS memory_item,
        argMaxIf(mutated, ingested_at, row_type = 'item') AS mutated,
        argMaxIf(unmodifiable, ingested_at, row_type = 'item') AS unmodifiable,
        argMaxIf(unmodifiable_except_chaos, ingested_at, row_type = 'item') AS unmodifiable_except_chaos,
        argMaxIf(is_relic, ingested_at, row_type = 'item') AS is_relic,
        argMaxIf(replica, ingested_at, row_type = 'item') AS replica,
        argMaxIf(extended_prefixes, ingested_at, row_type = 'item') AS extended_prefixes,
        argMaxIf(extended_suffixes, ingested_at, row_type = 'item') AS extended_suffixes,
        argMaxIf(hybrid_base_type_name, ingested_at, row_type = 'item') AS hybrid_base_type_name,
        argMaxIf(hybrid_is_vaal_gem, ingested_at, row_type = 'item') AS hybrid_is_vaal_gem,
        argMaxIf(hybrid_sec_descr_text, ingested_at, row_type = 'item') AS hybrid_sec_descr_text,
        argMaxIf(incubated_item_name, ingested_at, row_type = 'item') AS incubated_item_name,
        argMaxIf(incubated_item_level, ingested_at, row_type = 'item') AS incubated_item_level,
        argMaxIf(incubated_item_progress, ingested_at, row_type = 'item') AS incubated_item_progress,
        argMaxIf(incubated_item_total, ingested_at, row_type = 'item') AS incubated_item_total,
        argMaxIf(influence_crusader, ingested_at, row_type = 'item') AS influence_crusader,
        argMaxIf(influence_elder, ingested_at, row_type = 'item') AS influence_elder,
        argMaxIf(influence_hunter, ingested_at, row_type = 'item') AS influence_hunter,
        argMaxIf(influence_redeemer, ingested_at, row_type = 'item') AS influence_redeemer,
        argMaxIf(influence_shaper, ingested_at, row_type = 'item') AS influence_shaper,
        argMaxIf(influence_warlord, ingested_at, row_type = 'item') AS influence_warlord,
        arraySort(x -> (x.1, x.2), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), toUInt32(ifNull(value_ordinal, 0)), attribute_name, attribute_display_mode, attribute_type, attribute_suffix, attribute_progress, attribute_value_text, attribute_value_style), (row_type = 'item_attribute') AND (array_group = 'properties'))) AS properties,
        arraySort(x -> (x.1, x.2), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), toUInt32(ifNull(value_ordinal, 0)), attribute_name, attribute_display_mode, attribute_type, attribute_suffix, attribute_progress, attribute_value_text, attribute_value_style), (row_type = 'item_attribute') AND (array_group = 'requirements'))) AS requirements,
        arraySort(x -> (x.1, x.2), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), toUInt32(ifNull(value_ordinal, 0)), attribute_name, attribute_display_mode, attribute_type, attribute_suffix, attribute_progress, attribute_value_text, attribute_value_style), (row_type = 'item_attribute') AND (array_group = 'additionalProperties'))) AS additional_properties,
        arraySort(x -> (x.1, x.2), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), toUInt32(ifNull(value_ordinal, 0)), attribute_name, attribute_display_mode, attribute_type, attribute_suffix, attribute_progress, attribute_value_text, attribute_value_style), (row_type = 'item_attribute') AND (array_group = 'nextLevelRequirements'))) AS next_level_requirements,
