        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'explicitMods') AND (mod_text IS NOT NULL)))) AS explicit_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'implicitMods') AND (mod_text IS NOT NULL)))) AS implicit_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'enchantMods') AND (mod_text IS NOT NULL)))) AS enchant_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'fracturedMods') AND (mod_text IS NOT NULL)))) AS fractured_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'craftedMods') AND (mod_text IS NOT NULL)))) AS crafted_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'utilityMods') AND (mod_text IS NOT NULL)))) AS utility_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'veiledMods') AND (mod_text IS NOT NULL)))) AS veiled_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'flavourText') AND (mod_text IS NOT NULL)))) AS flavour_text_lines,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'mutatedMods') AND (mod_text IS NOT NULL)))) AS mutated_mods,
        arrayMap(x -> (x.2), arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'item_mod') AND (array_group = 'hybrid.explicitMods') AND (mod_text IS NOT NULL)))) AS hybrid_explicit_mods,
        arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), socket_group, socket_attr, socket_colour), row_type = 'item_socket')) AS sockets,
        arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), ultimatum_type, ultimatum_tier), row_type = 'item_ultimatum_mod')) AS ultimatum_mods,
        arraySort(x -> (x.1, x.2), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), toUInt32(ifNull(value_ordinal, 0)), logbook_name, logbook_faction_id, logbook_faction_name, mod_text), row_type = 'item_logbook_mod')) AS logbook_mods,
        arraySort(x -> (x.1), groupUniqArrayIf((toUInt32(ifNull(array_ordinal, 0)), item_id, item_name, item_type_line, base_type, rarity, inventory_id, note, icon, ilvl, stack_size, frame_type, socket_index, verified, identified, corrupted, fractured), row_type = 'socketed_item')) AS socketed_items,
        arraySort(x -> (x.1, x.2, x.3, x.4), groupUniqArrayIf((item_id, array_group, toUInt32(ifNull(array_ordinal, 0)), toUInt32(ifNull(value_ordinal, 0)), attribute_name, attribute_display_mode, attribute_type, attribute_suffix, attribute_progress, attribute_value_text, attribute_value_style), row_type = 'socketed_item_attribute')) AS socketed_item_properties,
        arraySort(x -> (x.1, x.2, x.3), groupUniqArrayIf((item_id, array_group, toUInt32(ifNull(array_ordinal, 0)), mod_text), (row_type = 'socketed_item_mod') AND (mod_text IS NOT NULL))) AS socketed_item_mods
    FROM poe_trade.bronze_public_stash_mirage
    WHERE root_item_id IS NOT NULL
    GROUP BY
        checkpoint,
        root_item_id
)
